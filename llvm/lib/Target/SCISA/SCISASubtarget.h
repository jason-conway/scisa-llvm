#ifndef LLVM_LIB_TARGET_SCISA_SCISASUBTARGET_H
#define LLVM_LIB_TARGET_SCISA_SCISASUBTARGET_H

#include "SCISAFrameLowering.h"
#include "SCISAISelLowering.h"
#include "SCISAInstrInfo.h"
#include "SCISARegisterInfo.h"
#include "SCISASelectionDAGInfo.h"
#include "llvm/CodeGen/GlobalISel/CallLowering.h"
#include "llvm/CodeGen/GlobalISel/InstructionSelector.h"
#include "llvm/CodeGen/GlobalISel/LegalizerInfo.h"
#include "llvm/CodeGen/RegisterBankInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "SCISAGenSubtargetInfo.inc"

namespace llvm {

class StringRef;

class SCISASubtarget : public SCISAGenSubtargetInfo {
    virtual void anchor();
    SCISAInstrInfo InstrInfo;
    SCISAFrameLowering FrameLowering;
    SCISATargetLowering TLInfo;
    SCISASelectionDAGInfo TSInfo;

private:
    void initializeEnvironment();
    void initSubtargetFeatures(StringRef CPU, StringRef FS);

protected:
    std::unique_ptr<CallLowering> CallLoweringInfo;
    std::unique_ptr<InstructionSelector> InstSelector;
    std::unique_ptr<LegalizerInfo> Legalizer;
    std::unique_ptr<RegisterBankInfo> RegBankInfo;

public:
    // This constructor initializes the data members to match that
    // of the specified triple.
    SCISASubtarget(const Triple &TT, const std::string &CPU, const std::string &FS, const TargetMachine &TM);

    SCISASubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

    // ParseSubtargetFeatures - Parses features string setting specified
    // subtarget options.  Definition of function is auto generated by tblgen.
    void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);
 
    const SCISAInstrInfo *getInstrInfo() const override
    {
        return &InstrInfo;
    }
    const SCISAFrameLowering *getFrameLowering() const override
    {
        return &FrameLowering;
    }
    const SCISATargetLowering *getTargetLowering() const override
    {
        return &TLInfo;
    }
    const SCISASelectionDAGInfo *getSelectionDAGInfo() const override
    {
        return &TSInfo;
    }
    const SCISARegisterInfo *getRegisterInfo() const override
    {
        return &InstrInfo.getRegisterInfo();
    }
};

} // namespace llvm

#endif
